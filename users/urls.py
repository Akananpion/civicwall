# users/urls.py
from django.urls import path
from django.contrib.auth import views as auth_views
from .api_views import UserRegistrationAPIView, UserProfileAPIView
from . import views

urlpatterns = [
    path('register/', views.register, name='register'),
    path('login/', auth_views.LoginView.as_view(template_name='users/login.html'), name='login'),
    path('logout/', auth_views.LogoutView.as_view(), name='logout'),
    path('profile/', views.profile, name='profile'),
    path('profile/picture/', views.update_profile_picture, name='update_profile_picture'),
    path('password_reset/', auth_views.PasswordResetView.as_view(template_name='users/password_reset.html'), name='password_reset'),
    path('password_reset/done/', auth_views.PasswordResetDoneView.as_view(template_name='users/password_reset_done.html'), name='password_reset_done'),
    path('reset/<uidb64>/<token>/', auth_views.PasswordResetConfirmView.as_view(template_name='users/password_reset_confirm.html'), name='password_reset_confirm'),
    path('reset/done/', auth_views.PasswordResetCompleteView.as_view(template_name='users/password_reset_complete.html'), name='password_reset_complete'),
]


urlpatterns += [
    path('api/register/', UserRegistrationAPIView.as_view(), name='api_register'),
    path('api/profile/', UserProfileAPIView.as_view(), name='api_profile'),
]